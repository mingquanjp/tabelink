-- ==============================================================================
-- TABELINK - FINAL POSTGRESQL SCHEMA
-- ==============================================================================
-- Notes:
-- - Guest is not stored in DB. Guest means unauthenticated visitor.
-- - Customer actions reference CUSTOMER_PROFILE.
-- - Owner actions reference OWNER_PROFILE.
-- - Admin actions reference USER_ACCOUNT; the application should enforce Role = 'Admin'.
-- - Cross-table business rules are enforced where practical with composite foreign keys.

-- ==============================================================================
-- 1. BASE ACCOUNTS AND MASTER DATA
-- ==============================================================================

CREATE TABLE USER_ACCOUNT (
    AccountID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Email VARCHAR(255) NOT NULL,
    PasswordHash VARCHAR(255) NOT NULL,
    Role VARCHAR(50) NOT NULL CHECK (Role IN ('Admin', 'User', 'Owner')),
    Status VARCHAR(50) NOT NULL DEFAULT 'Pending'
        CHECK (Status IN ('Active', 'Banned', 'Pending', 'Disabled')),
    CreatedAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UpdatedAt TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CHECK (BTRIM(Email) <> '')
);

CREATE UNIQUE INDEX ux_user_account_email_lower
    ON USER_ACCOUNT (LOWER(Email));

CREATE INDEX idx_user_account_role ON USER_ACCOUNT(Role);
CREATE INDEX idx_user_account_status ON USER_ACCOUNT(Status);

CREATE TABLE FEATURE_MASTER (
    FeatureID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FeatureCode VARCHAR(100) UNIQUE NOT NULL,
    FeatureNameVN VARCHAR(255) NOT NULL,
    FeatureNameJP VARCHAR(255) NOT NULL
);

CREATE TABLE PAYMENT_METHOD (
    PaymentMethodID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MethodCode VARCHAR(100) UNIQUE NOT NULL,
    MethodName VARCHAR(255) NOT NULL
);

CREATE TABLE HASHTAG (
    TagID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name VARCHAR(100) UNIQUE NOT NULL
);

CREATE TABLE SPECIAL_REQUEST_TEMPLATE (
    TemplateID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TextVN TEXT NOT NULL,
    TextJP TEXT NOT NULL,
    RequestType VARCHAR(50) NOT NULL
        CHECK (RequestType IN ('Coriander', 'LessSpicy', 'VATInvoice', 'Other'))
);

CREATE TABLE BADGE_MASTER (
    BadgeID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BadgeCode VARCHAR(100) UNIQUE NOT NULL,
    BadgeNameVN VARCHAR(255) NOT NULL,
    BadgeNameJP VARCHAR(255) NOT NULL,
    DescriptionVN TEXT,
    DescriptionJP TEXT,
    Criteria TEXT
);

